-- 15 Perecedero
CREATE TABLE Perecederos(
  ID NUMBER NOT NULL FOREIGN KEY REFERENCES Productos(ID),
  FECHA_VENCIMIENTO DATE NOT NULL,
  PRECIO_UNIDAD_MEDIDA NUMBER NOT NULL,
  TIPO NUMBER
);
-- 16 NoPerecedero
CREATE TABLE NoPerecederos(
  ID NUMBER NOT NULL FOREIGN KEY REFERENCES Productos(ID),
  PRECIO_UNITARIO NUMBER NOT NULL,
  TIPO NUMBER
);
