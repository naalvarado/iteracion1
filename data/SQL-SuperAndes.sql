-- 5 Sucursal
CREATE TABLE Sucursales(
	ID NUMBER NOT NULL PRIMARY KEY,
	SUPERMERCADO NUMBER FOREIGN KEY REFERENCES Supermercados(ID),
	NOMBRE VARCHAR(255 BYTE) NOT NULL UNIQUE,
	CIUDAD VARCHAR(255 BYTE) NOT NULL,
	DIRECCION VARCHAR(255 BYTE) NOT NULL UNIQUE
 );
-- 6 Bodega

-- 7 Local
CREATE TABLE Locales(
 ID NUMBER NOT NULL PRIMARY KEY,
 SUCURSAL NUMBER FOREIGN KEY REFERENCES Sucursales(ID),
 DIRECCION VARCHAR(255 BYTE) NOT NULL UNIQUE
);
-- 8 Venta

-- 9 Estante
CREATE TABLE Estantes(
 ID NUMBER NOT NULL PRIMARY KEY,
 LOCAL NUMBER FOREIGN KEY REFERENCES Locales(ID),
 VOLUMEN NUMBER NOT NULL,
 MAX_PESO NUMBER NOT NULL
);
-- 10 Producto
CREATE TABLE Productos(
	ID NUMBER NOT NULL PRIMARY KEY,
	SUCURSAL NUMBER FOREIGN KEY REFERENCES Sucursales(ID),
	BODEGA NUMBER FOREIGN KEY REFERENCES Bodegas(ID),
	ESTANTE NUMBER FOREIGN KEY REFERENCES Estantes(ID),
	NOMBRE VARCHAR2(255 BYTE) NOT NULL,
	CODIGO VARCHAR2(255 BYTE) NOT NULL,
	MARCA VARCHAR2(255 BYTE) NOT NULL,
	PRESENTACION VARCHAR2(255 BYTE),
	CANTIDAD_PRESENTACION NUMBER,
	VOLUMEN_PAQUETE NUMBER,
	CALIFICACION_CALIDAD NUMBER
 );
